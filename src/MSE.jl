module MSE

export suma, producto

"""
    suma(a, b)

Devuelve la suma de `a` y `b`.

# Ejemplo
```julia
suma(2, 3)
# resultado: 5
```
"""
suma(a, b) = a + b

"""
    producto(a, b)

Devuelve el producto de `a` y `b`.

# Ejemplo
```julia
producto(2, 3)
# resultado: 6
```
"""
producto(a, b) = a * b

end # module